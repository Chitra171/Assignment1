package Assignments;

public class Month {

	public static void main(String[] args) {
		int number=3;
		
		switch(number)
		{
		case 1:
			System.out.println(" January");
			break;
		case 2:
			System.out.println("March");
			break;
		case 3:
			System.out.println("May");
			break;
		case 4:
			System.out.println("July");
			break;
		case 5:
			System.out.println(" September");
		break;
		 
		default:
			 System.out.println(" Invalid");
		 break;
		}
	}
}
			
			
	
	

